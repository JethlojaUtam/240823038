// 19. Number is Positive, Negative or Zero
// Definition: Check if number is +ve, -ve, or zero.
// Input: -5
// Output: Negative

const input=-5
if(input>0) {
    console.log(input,"is positive.")
}
else if(input<0) {
    console.log(input,"is negative.")
}
else {
    console.log(input,"is zero.")
}